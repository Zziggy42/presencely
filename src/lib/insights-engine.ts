/**
 * Computes the full discovery funnel from Google searches through to revenue.
 * Each stage shows the conversion rate to the next and vs industry average.
 */
export function computeDiscoveryFunnel(
  searchImpressions: number,
  mapsViews: number,
  directionRequests: number,
  conversionRate: number,
  avgSpend: number
): FunnelStage[] {
  const estimatedVisits = Math.round(directionRequests * conversionRate)
  const revenue = estimatedVisits * avgSpend
  const searchToMaps = Math.round((mapsViews / searchImpressions) * 100)
  const mapsToDirection = Math.round((directionRequests / mapsViews) * 100)
  const directionToVisit = Math.round(conversionRate * 100)

  return [
    {
      label: "Google Searches",
      value: searchImpressions,
      iconName: "Search",
      conversionToNext: searchToMaps,
      industryAvg: 28,
      aboveAvg: searchToMaps > 28,
      formatted: searchImpressions.toLocaleString(),
    },
    {
      label: "Maps Profile Views",
      value: mapsViews,
      iconName: "MapPin",
      conversionToNext: mapsToDirection,
      industryAvg: 15,
      aboveAvg: mapsToDirection > 15,
      formatted: mapsViews.toLocaleString(),
    },
    {
      label: "Direction Requests",
      value: directionRequests,
      iconName: "Navigation",
      conversionToNext: directionToVisit,
      industryAvg: 55,
      aboveAvg: directionToVisit > 55,
      formatted: directionRequests.toLocaleString(),
    },
    {
      label: "Estimated Visits",
      value: estimatedVisits,
      iconName: "Users",
      conversionToNext: null,
      industryAvg: null,
      aboveAvg: null,
      formatted: estimatedVisits.toLocaleString(),
    },
    {
      label: "Monthly Revenue",
      value: revenue,
      iconName: "DollarSign",
      conversionToNext: null,
      industryAvg: null,
      aboveAvg: null,
      formatted: `CHF ${revenue.toLocaleString()}`,
    },
  ]
}

/**
 * Computes loyalty radius and customer distance statistics.
 * Walk-in locals (<0.5 mi) rarely use Google directions — their true count
 * is estimated by scaling up the direction-request-based visitor count.
 */
export function computeLoyaltyRadius(
  distanceData: Array<{ range: string; pct: number }>,
  directionBasedVisitors: number
): LoyaltyStats {
  const walkInRate = distanceData.find((d) => d.range === "<0.5 km")?.pct ?? 38
  const destinationRate = distanceData
    .filter((d) => d.range === "5–10 km" || d.range === "10+ km")
    .reduce((sum, d) => sum + d.pct, 0)

  const midpoints: Record<string, number> = {
    "<0.5 km":  0.25,
    "0.5–2 km": 1.2,
    "2–5 km":   3.5,
    "5–10 km":  7.5,
    "10+ km":   15.0,
  }

  const avgDistanceMiles = distanceData.reduce((sum, d) => {
    return sum + (midpoints[d.range] ?? 5) * (d.pct / 100)
  }, 0)

  // 80th percentile loyalty radius
  let cumulative = 0
  let loyaltyRadius80pct = 5
  for (const d of distanceData) {
    cumulative += d.pct
    if (cumulative >= 80) {
      loyaltyRadius80pct = midpoints[d.range] ?? 5
      break
    }
  }

  // Walk-in locals rarely request directions, so direction-based count
  // only captures (100 - walkInRate)% of true visitors
  const estimatedTrueMonthlyVisitors = Math.round(
    directionBasedVisitors / ((100 - walkInRate) / 100)
  )

  return {
    avgDistance: Math.round(avgDistanceMiles * 10) / 10,
    loyaltyRadius80pct,
    walkInRate,
    destinationRate,
    estimatedTrueMonthlyVisitors,
  }
}

