export interface StaffingROI {
  laborCost: number
  revenue: number
  laborPct: number
  grossMargin: number
  overstaffCostPerExtraStaff: number   // daily cost of 1 unnecessary extra staff
  understaffRevenueLoss: number        // estimated 15% revenue loss if 1 staff short at peak
}

// ─── Staffing Intelligence Functions ─────────────────────────────────────────

/**
 * Computes minimum staff needed for a shift.
 * Uses ceil(visitors / (hours × capacityPerStaff)) with a minimum of 1.
 */
export function computeStaffNeeded(
  visitorsInShift: number,
  shiftHours: number,
  capacityPerStaffPerHour: number
): number {
  return Math.max(1, Math.ceil(visitorsInShift / (shiftHours * capacityPerStaffPerHour)))
}

/**
 * Computes total labour cost for a given number of staff-hours.
 */
export function computeLaborCost(staffHours: number, hourlyWage: number): number {
  return staffHours * hourlyWage
}

/**
 * Computes staffing ROI breakdown:
 *  - grossMargin after labour
 *  - daily cost of overstaffing by 1
 *  - revenue loss from understaffing by 1 at peak
 */
export function computeStaffingROI(
  revenue: number,
  laborCost: number,
  hourlyWage: number,
  openHours: number
): StaffingROI {
  return {
    laborCost,
    revenue,
    laborPct: Math.round((laborCost / revenue) * 100),
    grossMargin: revenue - laborCost,
    overstaffCostPerExtraStaff: Math.round(hourlyWage * openHours),
    understaffRevenueLoss: Math.round(revenue * 0.15),
  }
}
