// ─── Types ─────────────────────────────────────────────────────────────────

export type ImpactTier = "Peak" | "High" | "Medium" | "Low"
export type FactorStatus = "resolved" | "pending"
export type MetricCategory =
  | "direction_requests"
  | "organic_clicks"
  | "rich_results"
  | "ctr"
  | "maps_views"
  | "discovery"

export interface ImpactFactor {
  id: string
  label: string
  lift: number          // fractional multiplier e.g. 0.18 = 18%
  category: MetricCategory
  confidence: number    // 0–100 confidence in the benchmark
  description: string   // one-line explanation
  affectsMetric: string // human-readable metric name
  status: FactorStatus
  estimatedLiftValue: number // extra direction requests this would add
  href: string          // where to fix it
}

export interface TrafficLift {
  baseline: number           // estimated monthly direction requests WITHOUT Presencely
  current: number            // actual current direction requests
  fullPotential: number      // if all actions are completed
  currentLift: number        // already gained from Presencely
  remainingLift: number      // still available if remaining actions done
}

export interface BusyDay {
  day: string
  searches: number
  maps: number
  rawScore: number
  score: number        // 0–100 normalised
  tier: ImpactTier
  recommendation: string
  staffingNote: string
}

export interface ROIResult {
  currentMonthlyVisits: number     // additional visits already generated by Presencely
  fullPotentialVisits: number      // additional visits if fully optimised
  currentRevenue: number           // $ already gained per month
  fullPotentialRevenue: number     // $ available if fully optimised
  pendingActionCount: number
}

