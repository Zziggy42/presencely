// ─── Algorithm functions ────────────────────────────────────────────────────

/**
 * Determines which factors are resolved vs pending, and estimates
 * the additional direction requests each factor could add.
 */
export function computeImpactBreakdown(
  seoIssues: Array<{ id: number; severity: string; title: string }>,
  recentReviews: Array<{ replied: boolean }>,
  currentDirections: number
): ImpactFactor[] {
  const unrepliedCount = recentReviews.filter((r) => !r.replied).length
  const hasCriticalSeo = seoIssues.some((i) => i.severity === "critical")
  const hasSchemaIssue = seoIssues.some((i) => i.title.toLowerCase().includes("schema"))
  const hasSpeedIssue = seoIssues.some((i) => i.title.toLowerCase().includes("load"))

  const statusMap: Record<string, FactorStatus> = {
    reply_reviews: unrepliedCount > 0 ? "pending" : "resolved",
    fix_seo:       hasCriticalSeo     ? "pending" : "resolved",
    schema_markup: hasSchemaIssue     ? "pending" : "resolved",
    page_speed:    hasSpeedIssue      ? "pending" : "resolved",
    gbp_posts:     "pending", // No GBP posting data available — treat as opportunity
    complete_gbp:  "pending", // No GBP completeness score — treat as opportunity
  }

  return RAW_FACTORS.map((factor) => {
    const status = statusMap[factor.id]
    const estimatedLiftValue = Math.round(currentDirections * factor.lift)
    return { ...factor, status, estimatedLiftValue }
  })
}

/**
 * Computes traffic lift figures comparing Presencely vs no software.
 *
 * Model:
 *  - "baseline" uses the MEASURED visibility gap (from the projection chart) to
 *    back-calculate where the business would be without the software. This anchors
 *    the ROI to real observed data rather than resolved-action assumptions.
 *  - "fullPotential" = current × (1 + pendingLift) if all remaining actions done.
 *
 * @param visibilityGapPercent - measured % gap between withPresencely and withoutPresencely
 *   at the latest historical data point (e.g. 36 means 36% higher with the software).
 */
export function computeTrafficLift(
  currentDirections: number,
  factors: ImpactFactor[],
  visibilityGapPercent: number
): TrafficLift {
  // Use measured gap to determine baseline
  const baseline = Math.round(currentDirections / (1 + visibilityGapPercent / 100))
  const currentLift = currentDirections - baseline

  // Full potential = current × (1 + sum of pending factor lifts)
  const pendingLift = factors
    .filter((f) => f.status === "pending")
    .reduce((sum, f) => sum + f.lift, 0)

  const fullPotential = Math.round(currentDirections * (1 + pendingLift))
  const remainingLift = fullPotential - currentDirections

  return { baseline, current: currentDirections, fullPotential, currentLift, remainingLift }
}

/**
 * Converts direction-request lift to estimated visits and revenue.
 */
export function computeROI(
  trafficLift: TrafficLift,
  avgSpend: number,
  conversionRate: number,
  pendingFactors: ImpactFactor[]
): ROIResult {
  const currentMonthlyVisits     = Math.round(trafficLift.currentLift   * conversionRate)
  const fullPotentialVisits      = Math.round((trafficLift.currentLift + trafficLift.remainingLift) * conversionRate)
  const currentRevenue           = Math.round(currentMonthlyVisits  * avgSpend)
  const fullPotentialRevenue     = Math.round(fullPotentialVisits    * avgSpend)
  const pendingActionCount       = pendingFactors.filter((f) => f.status === "pending").length

  return { currentMonthlyVisits, fullPotentialVisits, currentRevenue, fullPotentialRevenue, pendingActionCount }
}

/**
 * Scores each day of the week for predicted foot-traffic busyness.
 * Score = (searches × 0.4) + (maps × 0.6), normalised 0–100.
 * Maps clicks weighted higher — they signal stronger intent to visit.
 */
export function computeBusiestDays(
  weeklySearchData: Array<{ day: string; searches: number; maps: number }>
): BusyDay[] {
  const withScores = weeklySearchData.map((d) => ({
    ...d,
    rawScore: d.searches * 0.4 + d.maps * 0.6,
  }))

  const maxScore = Math.max(...withScores.map((d) => d.rawScore))

  return withScores.map((d) => {
    const score = Math.round((d.rawScore / maxScore) * 100)
    const tier: ImpactTier =
      score >= 85 ? "Peak" :
      score >= 65 ? "High" :
      score >= 40 ? "Medium" : "Low"

    return {
      ...d,
      score,
      tier,
      ...TIER_RECOMMENDATIONS[tier],
    }
  })
}

/**
 * Overall confidence in the predictions — weighted average of
 * pending factor confidence scores.
 */
export function computeOverallConfidence(factors: ImpactFactor[]): number {
  const pending = factors.filter((f) => f.status === "pending")
  if (pending.length === 0) return 95
  const avg = pending.reduce((sum, f) => sum + f.confidence, 0) / pending.length
  return Math.round(avg)
}

/**
 * Percentage gap between Presencely trajectory and the no-software baseline
 * at the latest historical data point.
 */
export function computeVisibilityGapPercent(
  projectionData: Array<{ withPresencely: number; withoutPresencely: number; projected: boolean }>
): number {
  const lastHistorical = [...projectionData].reverse().find((d) => !d.projected)
  if (!lastHistorical) return 0
  return Math.round(
    ((lastHistorical.withPresencely - lastHistorical.withoutPresencely) /
      lastHistorical.withoutPresencely) * 100
  )
}

